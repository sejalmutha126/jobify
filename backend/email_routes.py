# ==================== GET EMAIL CAMPAIGNS ====================

@router.get("/campaigns")
def get_email_campaigns(
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db)
):
    """
    Get user's email campaigns.
    """
    
    campaigns = db.query(EmailCampaign).filter(
        EmailCampaign.user_id == current_user.id
    ).limit(50).all()
    
    return {
        'total_campaigns': len(campaigns),
        'campaigns': [
            {
                'id': c.id,
                'recruiter_email': c.recruiter_email,
                'sent_date': c.sent_date,
                'opened': c.opened,
                'replied': c.replied
            }
            for c in campaigns
        ]
    }

# ==================== UPDATE CAMPAIGN STATUS ====================

@router.put("/campaigns/{campaign_id}")
def update_campaign_status(
    campaign_id: int,
    opened: bool = None,
    replied: bool = None,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db)
):
    """
    Update campaign status (opened, replied).
    """
    
    try:
        campaign = db.query(EmailCampaign).filter(
            EmailCampaign.id == campaign_id,
            EmailCampaign.user_id == current_user.id
        ).first()
        
        if not campaign:
            raise HTTPException(status_code=404, detail="Campaign not found")
        
        if opened is not None:
            campaign.opened = opened
        
        if replied is not None:
            campaign.replied = replied
        
        db.commit()
        
        logger.info(f"Campaign {campaign_id} status updated")
        
        return {
            'success': True,
            'message': 'Campaign status updated',
            'campaign_id': campaign_id
        }
    
    except Exception as e:
        db.rollback()
        logger.error(f"Campaign update error: {str(e)}")
        raise HTTPException(status_code=500, detail="Failed to update campaign")


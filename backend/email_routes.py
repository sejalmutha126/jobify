logger = logging.getLogger(__name__)
router = APIRouter(prefix="/email", tags=["Email Automation"])

# ==================== GENERATE EMAIL ====================

@router.post("/generate", response_model=EmailAutomationResponse)
def generate_email(
    request: EmailAutomationRequest,
    current_user: User = Depends(get_current_user)
):
    """
    Generate a personalized cold email for recruiter.
    """
    
    try:
        # Prepare skills text
        skills_text = ", ".join(request.user_skills[:5]) if request.user_skills else "relevant skills"
        
        # Generate email body
        email_body = f"""Hi {request.recruiter_name},

I hope this email finds you well. I'm reaching out because I'm very interested in the {request.job_title} position at {request.company_name}.

With my background in {skills_text}, I believe I can make a meaningful contribution to your team and help drive success for your organization.

I've attached my resume for your reference and would love to discuss how my skills and experience align with your needs.

Thank you for considering my application. I look forward to hearing from you.

Best regards,
{request.user_name}"""
        
        subject = f"Application for {request.job_title} Position at {request.company_name}"
        
        logger.info(f"Email generated by {current_user.email} for {request.recruiter_email}")
        
        return EmailAutomationResponse(
            email_body=email_body,
            subject=subject
        )
    
    except Exception as e:
        logger.error(f"Email generation error: {str(e)}")
        raise HTTPException(status_code=500, detail="Failed to generate email")

